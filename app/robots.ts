import { MetadataRoute } from 'next'

export default function robots(): MetadataRoute.Robots {
  return {
    rules: [
      {
        userAgent: '*',
        allow: '/',
        disallow: ['/admin/', '/api/', '/checkout/', '/account/', '/login', '/register'],
      },
    ],
    sitemap: 'https://faith-shop.fr/sitemap.xml',
  }
}
